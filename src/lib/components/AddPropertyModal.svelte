<script lang="ts">
  import { DatabaseService } from '$lib/services/databaseService';
  import type { City } from '$lib/types/database';
  import { onMount } from 'svelte';

  interface Props {
    onClose: () => void;
    onPropertyAdded: () => void;
  }

  let { onClose, onPropertyAdded }: Props = $props();

  let name = $state('');
  let city = $state('');
  let notes = $state('');
  let cities = $state<City[]>([]);
  let showCityDropdown = $state(false);
  let isSubmitting = $state(false);
  let error = $state('');

  // Load cities on mount
  onMount(async () => {
    try {
      cities = await DatabaseService.getCities();
    } catch (err) {
      console.error('Failed to load cities:', err);
    }
  });

  // Use $derived instead of $effect for computed values
  let filteredCities = $derived.by(() => {
    if (city.trim()) {
      return cities.filter((c) => c.name.toLowerCase().includes(city.toLowerCase())).slice(0, 5);
    } else {
      return cities.slice(0, 5);
    }
  });

  // Handle city input focus
  function handleCityFocus() {
    showCityDropdown = filteredCities.length > 0;
  }

  // Handle city input changes
  function handleCityInput() {
    showCityDropdown = city.trim() ? filteredCities.length > 0 : false;
  }

  function selectCity(cityName: string) {
    city = cityName;
    showCityDropdown = false;
  }

  async function handleSubmit() {
    if (!name.trim() || !city.trim()) {
      error = 'Please fill in all required fields';
      return;
    }

    try {
      isSubmitting = true;
      error = '';

      const result = await DatabaseService.createProperty(
        name.trim().toUpperCase(),
        city.trim().toUpperCase(),
        notes.trim() || undefined
      );

      if (result.success) {
        onPropertyAdded();
      } else {
        error = result.error || 'Failed to create property';
      }
    } catch (err) {
      console.error('Error creating property:', err);
      error = 'Failed to create property';
    } finally {
      isSubmitting = false;
    }
  }

  function handleKeydown(event: KeyboardEvent) {
    if (event.key === 'Escape') {
      onClose();
    }
  }
</script>

<svelte:window onkeydown={handleKeydown} />

<div class="bg-opacity-50 fixed inset-0 z-50 flex items-center justify-center bg-black p-4">
  <div
    class="bg-background-50 border-background-200 max-h-[90vh] w-full max-w-md overflow-y-auto rounded-xl border shadow-xl"
  >
    <!-- Modal Header -->
    <div class="border-background-200 flex items-center justify-between border-b px-5 py-4">
      <h2 class="text-foreground-900 text-base font-semibold">Add New Property</h2>
      <button
        onclick={onClose}
        class="text-foreground-500 hover:text-foreground-700 transition-colors"
        title="Close"
      >
        <svg class="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path
            stroke-linecap="round"
            stroke-linejoin="round"
            stroke-width="2"
            d="M6 18L18 6M6 6l12 12"
          />
        </svg>
      </button>
    </div>

    <!-- Modal Content -->
    <form onsubmit={handleSubmit} class="space-y-5 p-5">
      <!-- Property Name -->
      <div>
        <label class="text-foreground-700 mb-1.5 block text-xs font-medium">
          Property Name <span class="text-red-600">*</span>
        </label>
        <input
          type="text"
          bind:value={name}
          placeholder="e.g., Apartment 85sqm"
          class="border-background-300 bg-background-100 text-foreground-900 placeholder-foreground-500 focus:ring-accent-500 focus:border-accent-500 w-full rounded-md border px-3 py-2 text-sm transition-colors focus:ring-1 focus:outline-none"
          required
        />
      </div>

      <!-- City with Autocomplete -->
      <div class="relative">
        <label class="text-foreground-700 mb-1.5 block text-xs font-medium">
          City <span class="text-red-600">*</span>
        </label>
        <input
          type="text"
          bind:value={city}
          oninput={handleCityInput}
          onfocus={handleCityFocus}
          onblur={() => setTimeout(() => (showCityDropdown = false), 200)}
          placeholder="e.g., Athens"
          class="border-background-300 bg-background-100 text-foreground-900 placeholder-foreground-500 focus:ring-accent-500 focus:border-accent-500 w-full rounded-md border px-3 py-2 text-sm transition-colors focus:ring-1 focus:outline-none"
          required
        />

        <!-- City Dropdown -->
        {#if showCityDropdown}
          <div
            class="border-background-300 bg-background-50 absolute z-10 mt-1 max-h-48 w-full overflow-y-auto rounded-md border shadow-lg"
          >
            {#each filteredCities as cityOption}
              <button
                type="button"
                onclick={() => selectCity(cityOption.name)}
                class="hover:bg-background-100 flex w-full items-center justify-between px-3 py-2 text-left text-sm transition-colors"
              >
                <span class="text-foreground-900 font-medium">{cityOption.name}</span>
                <span class="text-foreground-500 text-xs">
                  {cityOption.usageCount}
                </span>
              </button>
            {/each}

            {#if filteredCities.length === 0 && city.trim()}
              <div class="px-3 py-2 text-center text-xs text-foreground-500">
                New city will be created
              </div>
            {/if}
          </div>
        {/if}
      </div>

      <!-- Notes -->
      <div>
        <label class="text-foreground-700 mb-1.5 block text-xs font-medium">
          Notes <span class="text-foreground-500">(Optional)</span>
        </label>
        <textarea
          bind:value={notes}
          placeholder="Additional details..."
          rows="3"
          class="border-background-300 bg-background-100 text-foreground-900 placeholder-foreground-500 focus:ring-accent-500 focus:border-accent-500 w-full resize-none rounded-md border px-3 py-2 text-sm transition-colors focus:ring-1 focus:outline-none"
        ></textarea>
      </div>

      <!-- Error Message -->
      {#if error}
        <div class="rounded-lg border border-red-300 bg-red-50 px-3 py-2">
          <p class="text-sm text-red-800">{error}</p>
        </div>
      {/if}

      <!-- Actions -->
      <div class="border-background-200 flex items-center justify-end gap-2 border-t pt-4">
        <button
          type="button"
          onclick={onClose}
          disabled={isSubmitting}
          class="border-background-300 bg-background-100 text-foreground-700 hover:bg-background-200 rounded-md border px-4 py-2 text-sm font-medium transition-colors disabled:opacity-50"
        >
          Cancel
        </button>
        <button
          type="submit"
          disabled={isSubmitting}
          class="bg-accent-500 hover:bg-accent-600 rounded-md px-4 py-2 text-sm font-medium text-white transition-colors disabled:cursor-not-allowed disabled:opacity-50"
        >
          {#if isSubmitting}
            Creating...
          {:else}
            Create Property
          {/if}
        </button>
      </div>
    </form>
  </div>
</div>
